    private void updateTable(){
        try{
            String query = "SELECT * FROM `attendance_table`";
            ps = conn.prepareStatement(query);
            rs = ps.executeQuery();
            jTable.setModel(DbUtils.resultSetToTableModel(rs));
            
            if(rs.next()){
                String fn = rs.getString(3);
                Name.firstName = fn;
            }
        }catch(Exception e){
            
        }finally{
            try{
                rs.close();
                ps.close();
                
            }catch(Exception e){
                
            }
        }
    }
    
    

